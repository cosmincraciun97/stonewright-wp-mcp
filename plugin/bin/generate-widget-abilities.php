<?php
/**
 * Widget ability generator — produces one ability class per widget plus the
 * two contract fixtures (positive + permission-error) the ContractTest
 * data-provider requires.
 *
 * Reads:    plugin/includes/Elementor/WidgetRegistry/manifest.json
 * Writes:   plugin/includes/Abilities/ElementorWidgets/Add<PascalCase>.php
 *           plugin/tests/fixtures/abilities/elementor-add-<slug>.json
 *           plugin/tests/fixtures/abilities/elementor-add-<slug>.error.json
 *           plugin/includes/Abilities/ElementorWidgets/_class_list.php
 *
 * Idempotent: re-runs are safe — files are overwritten with the
 * deterministic generated content. The `_class_list.php` file is the
 * authoritative class-string array AbilityRegistry::register_all()
 * merges into its list().
 *
 * Slug skip rules (kept in sync with widget-inventory-extract.php):
 *   - empty / null slug
 *   - "wp-widget-" trailing-dash anomaly emitted by the WP_Widget wrapper
 *
 * Usage: php plugin/bin/generate-widget-abilities.php
 */
declare(strict_types=1);

$repo_root      = realpath( __DIR__ . '/../..' );
$manifest_path  = $repo_root . '/plugin/includes/Elementor/WidgetRegistry/manifest.json';
$abilities_dir  = $repo_root . '/plugin/includes/Abilities/ElementorWidgets';
$fixtures_dir   = $repo_root . '/plugin/tests/fixtures/abilities';
$class_list_path = $abilities_dir . '/_class_list.php';

if ( ! is_dir( $abilities_dir ) ) {
	mkdir( $abilities_dir, 0777, true );
}
if ( ! is_dir( $fixtures_dir ) ) {
	mkdir( $fixtures_dir, 0777, true );
}

$raw = (string) file_get_contents( $manifest_path );
if ( substr( $raw, 0, 3 ) === "\xEF\xBB\xBF" ) {
	$raw = substr( $raw, 3 );
}
$manifest = json_decode( $raw, true );
if ( ! is_array( $manifest ) || empty( $manifest['widgets'] ) ) {
	fwrite( STDERR, "ERROR: invalid manifest at $manifest_path\n" );
	exit( 1 );
}

// Defaults used to populate "settings" in the positive contract fixture
// for required_for_render keys. Mirrors how the live editor's "add
// widget" UI seeds first-time controls.
$default_settings = [
	'title'                => 'Sample heading',
	'text'                 => 'Click me',
	'editor'               => '<p>Sample paragraph.</p>',
	'due_date'             => '2026-12-31 09:00',
	'image'                => [ 'id' => 1, 'url' => 'http://example.com/img.png' ],
	'selected_icon'        => [ 'value' => 'fas fa-star', 'library' => 'fa-solid' ],
	'icon_list'            => [ [ 'text' => 'Item 1' ] ],
	'social_icon_list'     => [ [ 'social_icon' => [ 'value' => 'fab fa-facebook', 'library' => 'fa-brands' ] ] ],
	'tabs'                 => [ [ 'tab_title' => 'Tab 1', 'tab_content' => '<p>Content</p>' ] ],
	'menu'                 => '5001',
	'space'                => [ 'unit' => 'px', 'size' => 50 ],
	'wp_gallery'           => [],
	'video_type'           => 'youtube',
	'percent'              => [ 'unit' => '%', 'size' => 50 ],
	'rating'               => 4,
	'features_list'        => [ [ 'item_text' => 'Feature 1' ] ],
	'price_list'           => [ [ 'title' => 'Item 1', 'price' => '10' ] ],
	'testimonial_content'  => 'Great service',
	'before_text'          => 'Welcome to',
	'carousel'             => [],
	'title_text'           => 'Title',
	'title_text_a'         => 'Side A',
	'title_text_b'         => 'Side B',
];

/** Convert a widget slug to a PascalCase class suffix. */
function pascal_case( string $slug ): string {
	$slug = preg_replace( '/[^A-Za-z0-9]+/', '_', $slug ) ?? '';
	$slug = trim( $slug, '_' );
	if ( $slug === '' ) {
		return 'Unknown';
	}
	$parts = explode( '_', $slug );
	$out   = '';
	foreach ( $parts as $p ) {
		if ( $p === '' ) {
			continue;
		}
		// Numbers: prefix with 'N' so we never start with a digit.
		if ( ctype_digit( $p ) ) {
			$out .= 'N' . $p;
			continue;
		}
		$out .= ucfirst( strtolower( $p ) );
	}
	return $out !== '' ? $out : 'Unknown';
}

$generated_classes  = [];
$skipped            = [];
$created_fixtures   = 0;
$created_classes    = 0;
$widget_class_map   = [];

foreach ( $manifest['widgets'] as $slug => $entry ) {
	if ( ! is_string( $slug ) || $slug === '' ) {
		$skipped[] = '(empty slug)';
		continue;
	}
	// Skip the WP_Widget wrapper anomaly.
	if ( $slug === 'wp-widget-' ) {
		$skipped[] = $slug;
		continue;
	}

	$pascal     = pascal_case( $slug );
	$class_name = 'Add' . $pascal;
	$class_fqn  = '\\Stonewright\\WpMcp\\Abilities\\ElementorWidgets\\' . $class_name;
	$file_path  = $abilities_dir . '/' . $class_name . '.php';

	$widget_class_map[ $slug ] = $class_fqn;
	$generated_classes[]       = $class_fqn;

	$title_safe = addcslashes( (string) ( $entry['title'] ?? $slug ), "'\\" );

	$class_php = <<<PHP
<?php
declare( strict_types=1 );

namespace Stonewright\\WpMcp\\Abilities\\ElementorWidgets;

/**
 * Generated by plugin/bin/generate-widget-abilities.php — do not edit
 * by hand. Re-run the generator after the manifest changes.
 *
 * Widget: {$title_safe} (slug: {$slug})
 */
final class {$class_name} extends WidgetAbilityBase {
	protected function slug(): string {
		return '{$slug}';
	}
}

PHP;

	file_put_contents( $file_path, $class_php );
	$created_classes++;

	// Build positive fixture.
	$required = is_array( $entry['required_for_render'] ?? null )
		? $entry['required_for_render']
		: [];
	$settings = [];
	foreach ( $required as $req_key ) {
		$settings[ $req_key ] = $default_settings[ $req_key ] ?? 'sample';
	}

	$positive_fixture = [
		'args'   => [
			'post_id'   => 1,
			'parent_id' => 'parent1',
			'settings'  => empty( $settings ) ? new \stdClass() : $settings,
		],
		'expect' => 'ok',
	];

	$error_fixture = [
		'args'    => [
			'post_id'   => 999999,
			'parent_id' => 'parent1',
			'settings'  => empty( $settings ) ? new \stdClass() : $settings,
		],
		'expect'  => 'wp_error',
	];

	$fixture_slug = 'elementor-add-' . $slug;
	file_put_contents(
		$fixtures_dir . '/' . $fixture_slug . '.json',
		json_encode( $positive_fixture, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE ) . "\n"
	);
	file_put_contents(
		$fixtures_dir . '/' . $fixture_slug . '.error.json',
		json_encode( $error_fixture, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE ) . "\n"
	);
	$created_fixtures += 2;
}

// Emit the canonical class list — AbilityRegistry::register_all() reads this.
sort( $generated_classes );
$entries_php = '';
foreach ( $generated_classes as $fqn ) {
	$entries_php .= "\t" . $fqn . "::class,\n";
}

$class_list_php = <<<PHP
<?php
/**
 * Generated by plugin/bin/generate-widget-abilities.php — do not edit
 * by hand. Re-run the generator after the manifest changes.
 *
 * @package Stonewright\\WpMcp
 *
 * @return array<int, class-string<\Stonewright\WpMcp\Abilities\Ability>>
 */

return [
$entries_php];

PHP;

file_put_contents( $class_list_path, $class_list_php );

fwrite( STDOUT, "Widget ability generation complete.\n" );
fwrite( STDOUT, "  classes:   $created_classes\n" );
fwrite( STDOUT, "  fixtures:  $created_fixtures\n" );
fwrite( STDOUT, "  skipped:   " . count( $skipped ) . ( empty( $skipped ) ? '' : ' (' . implode( ', ', $skipped ) . ')' ) . "\n" );
fwrite( STDOUT, "  class_list: $class_list_path\n" );
