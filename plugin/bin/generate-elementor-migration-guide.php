#!/usr/bin/env php
<?php
/**
 * Generate the exact legacy Elementor ability migration table.
 *
 * @package Stonewright\WpMcp
 */

declare( strict_types=1 );

$plugin_dir = dirname( __DIR__ );
$repo_root  = dirname( $plugin_dir );
$autoload   = $plugin_dir . '/vendor/autoload.php';
$class_list = $plugin_dir . '/includes/Abilities/ElementorWidgets/_class_list.php';
$output     = $repo_root . '/docs/migration-elementor-v3-tools.md';

if ( ! is_file( $autoload ) || ! is_file( $class_list ) ) {
	fwrite( STDERR, "Run composer install and generate the widget ability class list first.\n" );
	exit( 1 );
}

require_once $autoload;

if ( ! function_exists( '__' ) ) {
	function __( string $text, string $domain = '' ): string { // phpcs:ignore Generic.CodeAnalysis.UnusedFunctionParameter.FoundAfterLastUsed
		return $text;
	}
}

/** @var list<class-string> $classes */
$classes = include $class_list;
$rows    = [];
foreach ( $classes as $class ) {
	$ability = new $class();
	$rows[]  = '| `' . $ability->name() . '` | `stonewright/elementor-schema` → `stonewright/elementor-v3-batch-mutate` | Live widget schema, grouped write, mandatory readback |';
}

$body = <<<'MARKDOWN'
# Elementor V3 Legacy Tool Migration

This file is generated by `plugin/bin/generate-elementor-migration-guide.php`.
Do not edit the table by hand.

The 94 generated `stonewright/elementor-add-*` abilities are deprecated. They
remain available only through the explicit full/compatibility surface during
the warning window. The default surface does not expose them.

Use `stonewright/elementor-schema` to discover every widget installed in the
live Elementor runtime, including third-party add-ons. Then write through
`stonewright/elementor-v3-batch-mutate`, which validates exact live controls,
supports references and rollback, and verifies readback. For complete initial
pages, use `stonewright/elementor-v3-build-page-from-spec` before batch polish.

Removal cannot happen before two released warnings. With alpha.65 as release N,
the earliest removal release is alpha.67. Internal adapters and live schema
discovery remain after public aliases are removed.

| Deprecated ability | Exact replacement | Preserved behavior |
|---|---|---|
MARKDOWN;

$body .= "\n" . implode( "\n", $rows ) . "\n";
if ( false === file_put_contents( $output, $body ) ) {
	fwrite( STDERR, "Could not write {$output}.\n" );
	exit( 1 );
}

fwrite( STDOUT, 'Wrote ' . $output . ' with ' . count( $rows ) . " migrations.\n" );
